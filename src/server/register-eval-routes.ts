import { createReadStream, existsSync, readFileSync, statSync } from 'node:fs';
import path from 'node:path';

import express from 'express';

import { getSyntheticImage } from './synthetic-label-cache';
import {
  generateSyntheticLabel,
  getSyntheticGenerationModel,
  isSyntheticGenerationAvailable
} from './synthetic-label-generator';

const REPO_ROOT = path.resolve(
  path.dirname(new URL(import.meta.url).pathname),
  '..',
  '..'
);
const LABELS_ROOT = path.join(REPO_ROOT, 'evals/labels/assets');
const BATCH_MANIFEST_PATH = path.join(
  REPO_ROOT,
  'evals/batch/cola-cloud/manifest.json'
);
const BATCH_DIR = path.join(REPO_ROOT, 'evals/batch/cola-cloud');

type ManifestSet = {
  id: string;
  title: string;
  description: string;
  csvFile: string;
  imageCases: Array<{
    id: string;
    assetPath: string;
    beverageType: string;
    expectedRecommendation?: string;
    source: string;
  }>;
};

type Manifest = {
  sets: ManifestSet[];
};

function readManifest(): Manifest | null {
  if (!existsSync(BATCH_MANIFEST_PATH)) {
    return null;
  }

  try {
    return JSON.parse(readFileSync(BATCH_MANIFEST_PATH, 'utf8')) as Manifest;
  } catch {
    return null;
  }
}

type CsvRow = {
  filename: string;
  beverage_type?: string;
  brand_name?: string;
  fanciful_name?: string;
  class_type?: string;
  alcohol_content?: string;
  net_contents?: string;
  applicant_address?: string;
  origin?: string;
  country?: string;
  formula_id?: string;
  appellation?: string;
  vintage?: string;
};

/**
 * Minimal CSV parser tailored to our eval pack shape (no embedded quotes
 * or newlines in fields). The pack CSVs are generated by our own
 * fixtures pipeline and always comply with this limitation.
 */
function parseCsvRows(csv: string): CsvRow[] {
  const lines = csv.split(/\r?\n/).filter((line) => line.trim().length > 0);
  if (lines.length < 2) return [];
  const header = lines[0]!.split(',').map((h) => h.trim());
  return lines.slice(1).map((line) => {
    const cells = line.split(',').map((c) => c.trim());
    const row: Record<string, string> = {};
    header.forEach((h, i) => {
      row[h] = cells[i] ?? '';
    });
    return row as unknown as CsvRow;
  });
}

function mimeForFilename(filename: string): string {
  const lower = filename.toLowerCase();
  if (lower.endsWith('.webp')) return 'image/webp';
  if (lower.endsWith('.png')) return 'image/png';
  if (lower.endsWith('.jpg') || lower.endsWith('.jpeg')) return 'image/jpeg';
  if (lower.endsWith('.pdf')) return 'application/pdf';
  return 'application/octet-stream';
}

// ── COLA Cloud live-API proxy ────────────────────────────────────────
// Minimal server-side wrapper around the COLA Cloud REST API. We keep
// the X-API-Key header server-side so the browser never sees the key.
// Search results get a 10-minute in-memory cache so repeated "fetch
// fresh sample" clicks don't thrash the upstream rate limit — each
// click still picks a different random entry from the cached page.

const COLA_CLOUD_API_BASE = 'https://app.colacloud.us/api/v1';
const COLA_CLOUD_CACHE_TTL_MS = 10 * 60 * 1000;
let colaCloudCache: {
  fetchedAt: number;
  summaries: ColaCloudSummary[];
} | null = null;

type ColaCloudSummary = {
  ttb_id: string;
  brand_name: string;
  product_name: string;
  product_type: string;
  class_name: string;
  origin_name: string;
};

type ColaCloudImage = {
  ttb_image_id: string;
  image_index: number;
  container_position: string;
  extension_type: string;
  image_url: string;
};

type ColaCloudDetail = ColaCloudSummary & {
  abv: number | null;
  domestic_or_imported: string | null;
  grape_varietals: string[] | null;
  wine_vintage_year: number | null;
  wine_appellation: string | null;
  address_recipient: string | null;
  address_state: string | null;
  images: ColaCloudImage[];
};

async function fetchColaCloudJson<T>(
  endpoint: string,
  apiKey: string
): Promise<T> {
  const res = await fetch(`${COLA_CLOUD_API_BASE}${endpoint}`, {
    headers: { 'X-API-Key': apiKey }
  });
  if (!res.ok) {
    const body = await res.text();
    throw new Error(`COLA Cloud ${res.status}: ${body.slice(0, 200)}`);
  }
  return (await res.json()) as T;
}

function mapColaCloudToFields(detail: ColaCloudDetail) {
  const abvDisplay =
    detail.abv != null ? `${detail.abv.toFixed(1)}% Alc./Vol.` : '';
  const origin: 'domestic' | 'imported' =
    detail.domestic_or_imported === 'Imported' ? 'imported' : 'domestic';
  return {
    brandName: detail.brand_name ?? '',
    fancifulName: detail.product_name ?? '',
    classType: detail.class_name ?? '',
    alcoholContent: abvDisplay,
    netContents: '',
    applicantAddress: detail.address_recipient ?? '',
    origin,
    country: detail.origin_name ?? '',
    formulaId: detail.ttb_id ?? '',
    appellation: detail.wine_appellation ?? '',
    vintage: detail.wine_vintage_year ? String(detail.wine_vintage_year) : ''
  };
}

function normalizeBeverageType(raw: string): string {
  const lower = raw.toLowerCase();
  if (lower.includes('distilled')) return 'distilled-spirits';
  if (lower.includes('malt') || lower.includes('beer')) return 'malt-beverage';
  if (lower.includes('wine')) return 'wine';
  return 'unknown';
}

export function registerEvalRoutes(app: express.Express) {
  app.get('/api/eval/packs', (_request, response) => {
    const manifest = readManifest();
    if (!manifest) {
      response
        .status(404)
        .json({ kind: 'validation', message: 'Eval manifest unavailable.' });
      return;
    }

    const packs = manifest.sets.map((set) => ({
      id: set.id,
      title: set.title,
      description: set.description,
      csvFile: set.csvFile,
      imageCount: set.imageCases.length,
      images: set.imageCases.map((entry) => ({
        id: entry.id,
        source: entry.source,
        assetPath: entry.assetPath,
        filename: path.basename(entry.assetPath),
        beverageType: entry.beverageType,
        expectedRecommendation: entry.expectedRecommendation
      }))
    }));

    response.setHeader('cache-control', 'no-store');
    response.json({ packs });
  });

  app.get('/api/eval/pack/:packId/csv', (request, response) => {
    const manifest = readManifest();
    if (!manifest) {
      response
        .status(404)
        .json({ kind: 'validation', message: 'Eval manifest unavailable.' });
      return;
    }

    const set = manifest.sets.find((entry) => entry.id === request.params.packId);
    if (!set) {
      response
        .status(404)
        .json({ kind: 'validation', message: 'Unknown eval pack.' });
      return;
    }

    const csvPath = path.join(BATCH_DIR, set.csvFile);
    if (!existsSync(csvPath)) {
      response
        .status(404)
        .json({ kind: 'validation', message: 'CSV fixture missing on disk.' });
      return;
    }

    response.setHeader('content-type', 'text/csv; charset=utf-8');
    response.setHeader('cache-control', 'no-store');
    response.setHeader(
      'content-disposition',
      `attachment; filename="${set.csvFile}"`
    );
    response.send(readFileSync(csvPath));
  });

  /**
   * Returns one COLA Cloud sample — image-ready URL + application fields
   * parsed from the pack CSV — so the toolbench can populate both
   * the image dropzone and the intake form with one click. Defaults to
   * a random pick from the full 28-label cola-cloud-all set; pass
   * ?id=<rowId> to pick a specific label, or ?index=<n> for a
   * deterministic choice during demos.
   *
   * Shape is optimized for the client: everything the IntakeForm needs
   * is keyed as it expects (brandName, classType, alcoholContent, ...).
   */
  /**
   * Status probe for the live COLA Cloud integration. Returns whether
   * a key is configured (client uses this to decide if it should show
   * the "Fetch live sample" button at all).
   */
  app.get('/api/eval/cola-cloud/status', (_request, response) => {
    const available = Boolean(process.env.COLACLOUD_API_KEY?.trim());
    response.setHeader('cache-control', 'no-store');
    response.json({ available });
  });

  /**
   * Fetches a fresh COLA record from the live COLA Cloud API using the
   * server-side COLACLOUD_API_KEY. Returns an image-proxy URL + parsed
   * application fields in the same shape as /api/eval/sample, so the
   * client can treat both paths identically.
   *
   * Rate-limit aware: search results are cached for 10 minutes. Each
   * request picks a different random entry from the cached page.
   */
  app.get('/api/eval/cola-cloud/fresh', async (_request, response) => {
    const apiKey = process.env.COLACLOUD_API_KEY?.trim();
    if (!apiKey) {
      response
        .status(503)
        .json({ kind: 'validation', message: 'Live COLA Cloud key not configured.' });
      return;
    }

    try {
      const cacheValid =
        colaCloudCache !== null &&
        Date.now() - colaCloudCache.fetchedAt < COLA_CLOUD_CACHE_TTL_MS &&
        colaCloudCache.summaries.length > 0;

      if (!cacheValid) {
        // Pull a page of recent approvals. per_page=30 keeps us under
        // the COLA Cloud free-tier rate limit (~1 req/7s); one search
        // powers many "fresh" demo clicks.
        type SearchResponse = {
          data: ColaCloudSummary[];
        };
        const search = await fetchColaCloudJson<SearchResponse>(
          '/colas?per_page=30',
          apiKey
        );
        colaCloudCache = {
          fetchedAt: Date.now(),
          summaries: search.data
        };
      }

      const summaries = colaCloudCache!.summaries;
      if (summaries.length === 0) {
        response.status(502).json({ kind: 'validation', message: 'COLA Cloud returned no records.' });
        return;
      }

      // Random pick — different entry per click even when the search is
      // cached. Try up to 5 records to find one with at least one front
      // label image.
      type DetailResponse = { data: ColaCloudDetail };
      let detail: ColaCloudDetail | null = null;
      for (let attempt = 0; attempt < 5 && detail === null; attempt++) {
        const candidate = summaries[Math.floor(Math.random() * summaries.length)]!;
        try {
          const res = await fetchColaCloudJson<DetailResponse>(
            `/colas/${encodeURIComponent(candidate.ttb_id)}`,
            apiKey
          );
          if (res.data.images && res.data.images.length > 0) {
            detail = res.data;
          }
        } catch {
          // skip and try another
        }
      }
      if (!detail) {
        response.status(502).json({ kind: 'validation', message: 'No COLA records with images available right now.' });
        return;
      }

      // Prefer front first, then back; fall back to the raw order if
      // container_position is unset. COLA records often include both a
      // front and back label — return both so the reviewer sees the
      // full label content (warning + ingredients frequently live on
      // the back). Cap at two: review flow only accepts front + back.
      const positioned = (pos: string) =>
        detail.images.filter(
          (img) => img.container_position?.toLowerCase() === pos
        );
      const ordered = [
        ...positioned('front'),
        ...positioned('back'),
        ...detail.images.filter((img) => {
          const p = img.container_position?.toLowerCase();
          return p !== 'front' && p !== 'back';
        })
      ];
      const selectedImages = (ordered.length > 0 ? ordered : detail.images).slice(
        0,
        2
      );

      const fields = mapColaCloudToFields(detail);
      const beverageType = normalizeBeverageType(detail.product_type);

      // We don't expose the upstream image URL directly — it's signed
      // and tied to our API key's session. Proxy it via
      // /api/eval/cola-cloud/proxy-image so the client can fetch with
      // a normal cookie-less GET.
      const imagePayloads = selectedImages.map((img, idx) => {
        const position = img.container_position?.toLowerCase() || `img${idx + 1}`;
        return {
          id: `colacloud-${detail.ttb_id}-${position}`,
          source: 'cola-cloud-live',
          filename: `${detail.ttb_id}-${position}.${img.extension_type || 'jpg'}`,
          url: `/api/eval/cola-cloud/proxy-image?url=${encodeURIComponent(img.image_url)}&ttb=${encodeURIComponent(detail.ttb_id)}`,
          beverageType
        };
      });

      response.setHeader('cache-control', 'no-store');
      response.json({
        image: imagePayloads[0],
        images: imagePayloads,
        fields,
        meta: {
          ttbId: detail.ttb_id,
          approvedBrand: detail.brand_name,
          productName: detail.product_name
        }
      });
    } catch (error) {
      response.status(502).json({
        kind: 'validation',
        message: (error as Error).message.slice(0, 200)
      });
    }
  });

  /**
   * Image proxy for the live COLA Cloud path. The upstream image URLs
   * require the X-API-Key header; the browser can't hold that key, so
   * we fetch server-side and stream the bytes.
   */
  app.get('/api/eval/cola-cloud/proxy-image', async (request, response) => {
    const apiKey = process.env.COLACLOUD_API_KEY?.trim();
    if (!apiKey) {
      response.status(503).end();
      return;
    }
    const upstream = typeof request.query.url === 'string' ? request.query.url : '';
    if (!upstream || !upstream.startsWith('https://')) {
      response.status(400).end();
      return;
    }
    try {
      const upstreamRes = await fetch(upstream, {
        headers: { 'X-API-Key': apiKey }
      });
      if (!upstreamRes.ok || !upstreamRes.body) {
        response.status(upstreamRes.status).end();
        return;
      }
      // COLA Cloud's CDN frequently returns `binary/octet-stream` as the
      // content-type even for images. The upload pipeline downstream
      // (multer on /api/review) rejects that, so derive the real MIME
      // from the URL's extension and forward that instead.
      const upstreamContentType = upstreamRes.headers.get('content-type') ?? '';
      const urlExtMatch = upstream.match(/\.(webp|jpe?g|png|pdf)(?:$|[?#])/i);
      const sniffedContentType = urlExtMatch
        ? mimeForFilename(`x.${urlExtMatch[1]!.toLowerCase()}`)
        : null;
      const contentType =
        upstreamContentType.startsWith('image/') || upstreamContentType === 'application/pdf'
          ? upstreamContentType
          : sniffedContentType ?? 'image/jpeg';
      response.setHeader('content-type', contentType);
      response.setHeader('cache-control', 'private, max-age=300');
      const reader = upstreamRes.body.getReader();
      while (true) {
        const { value, done } = await reader.read();
        if (done) break;
        response.write(Buffer.from(value));
      }
      response.end();
    } catch {
      response.status(502).end();
    }
  });

  app.get('/api/eval/sample', (request, response) => {
    const manifest = readManifest();
    if (!manifest) {
      response.status(404).json({ kind: 'validation', message: 'Eval manifest unavailable.' });
      return;
    }
    const set = manifest.sets.find((entry) => entry.id === 'cola-cloud-all');
    if (!set) {
      response.status(404).json({ kind: 'validation', message: 'cola-cloud-all pack missing.' });
      return;
    }
    const csvPath = path.join(BATCH_DIR, set.csvFile);
    if (!existsSync(csvPath)) {
      response.status(404).json({ kind: 'validation', message: 'Pack CSV missing.' });
      return;
    }
    const csv = readFileSync(csvPath, 'utf8');
    const csvRows = parseCsvRows(csv);
    if (csvRows.length === 0) {
      response.status(404).json({ kind: 'validation', message: 'Pack CSV empty.' });
      return;
    }

    const requestedId = typeof request.query.id === 'string' ? request.query.id : null;
    const requestedIndex =
      typeof request.query.index === 'string'
        ? Number.parseInt(request.query.index, 10)
        : null;

    // Join CSV rows (keyed by filename) to manifest image cases (which
    // carry the beverage type + source subdir). The CSV is the source
    // of truth for application field values; the manifest is the source
    // of truth for the on-disk asset path.
    const manifestByFilename = new Map(
      set.imageCases.map((entry) => [path.basename(entry.assetPath), entry])
    );
    const joined = csvRows
      .map((row) => {
        const meta = manifestByFilename.get(row.filename);
        if (!meta) return null;
        return { row, meta };
      })
      .filter((entry): entry is { row: CsvRow; meta: ManifestSet['imageCases'][number] } => entry !== null);

    if (joined.length === 0) {
      response.status(404).json({ kind: 'validation', message: 'No matching manifest rows.' });
      return;
    }

    let picked: { row: CsvRow; meta: ManifestSet['imageCases'][number] };
    if (requestedId) {
      picked =
        joined.find(({ meta }) => meta.id === requestedId) ?? joined[0]!;
    } else if (requestedIndex != null && Number.isFinite(requestedIndex)) {
      const n = ((requestedIndex % joined.length) + joined.length) % joined.length;
      picked = joined[n]!;
    } else {
      picked = joined[Math.floor(Math.random() * joined.length)]!;
    }

    // Build the fields payload with the same keys the client's
    // IntakeForm uses.
    const { row, meta } = picked;
    const fields = {
      brandName: row.brand_name ?? '',
      fancifulName: row.fanciful_name ?? '',
      classType: row.class_type ?? '',
      alcoholContent: row.alcohol_content ?? '',
      netContents: row.net_contents ?? '',
      applicantAddress: row.applicant_address ?? '',
      origin: row.origin ?? '',
      country: row.country ?? '',
      formulaId: row.formula_id ?? '',
      appellation: row.appellation ?? '',
      vintage: row.vintage ?? ''
    };

    const image = {
      id: meta.id,
      source: meta.source,
      filename: path.basename(meta.assetPath),
      url: `/api/eval/label-image/${encodeURIComponent(meta.source)}/${encodeURIComponent(path.basename(meta.assetPath))}`,
      beverageType: meta.beverageType
    };

    response.setHeader('cache-control', 'no-store');
    response.json({ image, fields });
  });

  // Serves an image from the evals/labels/assets subtree.
  // `source` is a subdirectory like "cola-cloud" or "supplemental-generated".
  // `filename` is the base image filename. Path traversal is blocked.
  app.get('/api/eval/label-image/:source/:filename', (request, response) => {
    const { source, filename } = request.params;
    if (!source || !filename) {
      response.status(400).end();
      return;
    }

    if (
      source.includes('..') ||
      source.includes('/') ||
      source.includes('\\') ||
      filename.includes('..') ||
      filename.includes('/') ||
      filename.includes('\\')
    ) {
      response.status(400).end();
      return;
    }

    const resolved = path.join(LABELS_ROOT, source, filename);
    if (!resolved.startsWith(LABELS_ROOT)) {
      response.status(400).end();
      return;
    }

    if (!existsSync(resolved)) {
      response.status(404).end();
      return;
    }

    const stats = statSync(resolved);
    if (!stats.isFile()) {
      response.status(404).end();
      return;
    }

    response.setHeader('content-type', mimeForFilename(filename));
    response.setHeader('cache-control', 'public, max-age=3600');
    response.setHeader('content-length', String(stats.size));
    createReadStream(resolved).pipe(response);
  });

  registerSyntheticLabelRoutes(app);
}

// ─── Synthetic-label routes ──────────────────────────────────────────
// Toolbench "Generate sample with Gemini" button. Uses Imagen 4 to
// produce a fresh photorealistic label + matching declared fields,
// optionally with a baked-in defect so the verification pipeline's
// reject/review paths get exercised from a single click.

const SYNTHETIC_RATE_LIMIT_MS = 5_000;
const syntheticLastCallByIp = new Map<string, number>();

function registerSyntheticLabelRoutes(app: express.Express) {
  app.get('/api/eval/synthetic/status', (_request, response) => {
    response.setHeader('cache-control', 'no-store');
    const available = isSyntheticGenerationAvailable();
    const payload: { available: boolean; model?: string } = { available };
    if (available) payload.model = getSyntheticGenerationModel();
    response.json(payload);
  });

  app.post('/api/eval/synthetic/generate', async (request, response) => {
    if (!isSyntheticGenerationAvailable()) {
      response
        .status(503)
        .json({ kind: 'validation', message: 'Synthetic label generation is not configured on this server.' });
      return;
    }

    const ip = request.ip ?? 'unknown';
    const last = syntheticLastCallByIp.get(ip) ?? 0;
    const elapsed = Date.now() - last;
    if (elapsed < SYNTHETIC_RATE_LIMIT_MS) {
      const retryInMs = SYNTHETIC_RATE_LIMIT_MS - elapsed;
      response
        .status(429)
        .setHeader('retry-after', String(Math.ceil(retryInMs / 1000)))
        .json({ kind: 'validation', message: `Slow down — try again in ${Math.ceil(retryInMs / 1000)}s.` });
      return;
    }
    syntheticLastCallByIp.set(ip, Date.now());

    try {
      const result = await generateSyntheticLabel();
      response.setHeader('cache-control', 'no-store');
      response.json(result);
    } catch (err) {
      const message = err instanceof Error ? err.message : 'Synthetic label generation failed.';
      response
        .status(502)
        .json({ kind: 'adapter', message });
    }
  });

  app.get('/api/eval/synthetic/image/:id', (request, response) => {
    const { id } = request.params;
    if (!id || id.includes('..') || id.includes('/') || id.includes('\\')) {
      response.status(400).end();
      return;
    }
    const entry = getSyntheticImage(id);
    if (!entry) {
      response.status(404).end();
      return;
    }
    response.setHeader('content-type', entry.mime);
    response.setHeader('content-length', String(entry.bytes.length));
    response.setHeader('cache-control', 'private, max-age=900');
    response.send(entry.bytes);
  });
}
