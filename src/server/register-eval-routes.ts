import { createReadStream, existsSync, readFileSync, statSync } from 'node:fs';
import path from 'node:path';

import express from 'express';

const REPO_ROOT = path.resolve(
  path.dirname(new URL(import.meta.url).pathname),
  '..',
  '..'
);
const LABELS_ROOT = path.join(REPO_ROOT, 'evals/labels/assets');
const BATCH_MANIFEST_PATH = path.join(
  REPO_ROOT,
  'evals/batch/cola-cloud/manifest.json'
);
const BATCH_DIR = path.join(REPO_ROOT, 'evals/batch/cola-cloud');

type ManifestSet = {
  id: string;
  title: string;
  description: string;
  csvFile: string;
  imageCases: Array<{
    id: string;
    assetPath: string;
    beverageType: string;
    expectedRecommendation?: string;
    source: string;
  }>;
};

type Manifest = {
  sets: ManifestSet[];
};

function readManifest(): Manifest | null {
  if (!existsSync(BATCH_MANIFEST_PATH)) {
    return null;
  }

  try {
    return JSON.parse(readFileSync(BATCH_MANIFEST_PATH, 'utf8')) as Manifest;
  } catch {
    return null;
  }
}

type CsvRow = {
  filename: string;
  beverage_type?: string;
  brand_name?: string;
  fanciful_name?: string;
  class_type?: string;
  alcohol_content?: string;
  net_contents?: string;
  applicant_address?: string;
  origin?: string;
  country?: string;
  formula_id?: string;
  appellation?: string;
  vintage?: string;
};

/**
 * Minimal CSV parser tailored to our eval pack shape (no embedded quotes
 * or newlines in fields). The pack CSVs are generated by our own
 * fixtures pipeline and always comply with this limitation.
 */
function parseCsvRows(csv: string): CsvRow[] {
  const lines = csv.split(/\r?\n/).filter((line) => line.trim().length > 0);
  if (lines.length < 2) return [];
  const header = lines[0]!.split(',').map((h) => h.trim());
  return lines.slice(1).map((line) => {
    const cells = line.split(',').map((c) => c.trim());
    const row: Record<string, string> = {};
    header.forEach((h, i) => {
      row[h] = cells[i] ?? '';
    });
    return row as unknown as CsvRow;
  });
}

function mimeForFilename(filename: string): string {
  const lower = filename.toLowerCase();
  if (lower.endsWith('.webp')) return 'image/webp';
  if (lower.endsWith('.png')) return 'image/png';
  if (lower.endsWith('.jpg') || lower.endsWith('.jpeg')) return 'image/jpeg';
  if (lower.endsWith('.pdf')) return 'application/pdf';
  return 'application/octet-stream';
}

export function registerEvalRoutes(app: express.Express) {
  app.get('/api/eval/packs', (_request, response) => {
    const manifest = readManifest();
    if (!manifest) {
      response
        .status(404)
        .json({ kind: 'validation', message: 'Eval manifest unavailable.' });
      return;
    }

    const packs = manifest.sets.map((set) => ({
      id: set.id,
      title: set.title,
      description: set.description,
      csvFile: set.csvFile,
      imageCount: set.imageCases.length,
      images: set.imageCases.map((entry) => ({
        id: entry.id,
        source: entry.source,
        assetPath: entry.assetPath,
        filename: path.basename(entry.assetPath),
        beverageType: entry.beverageType,
        expectedRecommendation: entry.expectedRecommendation
      }))
    }));

    response.setHeader('cache-control', 'no-store');
    response.json({ packs });
  });

  app.get('/api/eval/pack/:packId/csv', (request, response) => {
    const manifest = readManifest();
    if (!manifest) {
      response
        .status(404)
        .json({ kind: 'validation', message: 'Eval manifest unavailable.' });
      return;
    }

    const set = manifest.sets.find((entry) => entry.id === request.params.packId);
    if (!set) {
      response
        .status(404)
        .json({ kind: 'validation', message: 'Unknown eval pack.' });
      return;
    }

    const csvPath = path.join(BATCH_DIR, set.csvFile);
    if (!existsSync(csvPath)) {
      response
        .status(404)
        .json({ kind: 'validation', message: 'CSV fixture missing on disk.' });
      return;
    }

    response.setHeader('content-type', 'text/csv; charset=utf-8');
    response.setHeader('cache-control', 'no-store');
    response.setHeader(
      'content-disposition',
      `attachment; filename="${set.csvFile}"`
    );
    response.send(readFileSync(csvPath));
  });

  /**
   * Returns one COLA Cloud sample — image-ready URL + application fields
   * parsed from the pack CSV — so the toolbench can populate both
   * the image dropzone and the intake form with one click. Defaults to
   * a random pick from the full 28-label cola-cloud-all set; pass
   * ?id=<rowId> to pick a specific label, or ?index=<n> for a
   * deterministic choice during demos.
   *
   * Shape is optimized for the client: everything the IntakeForm needs
   * is keyed as it expects (brandName, classType, alcoholContent, ...).
   */
  app.get('/api/eval/sample', (request, response) => {
    const manifest = readManifest();
    if (!manifest) {
      response.status(404).json({ kind: 'validation', message: 'Eval manifest unavailable.' });
      return;
    }
    const set = manifest.sets.find((entry) => entry.id === 'cola-cloud-all');
    if (!set) {
      response.status(404).json({ kind: 'validation', message: 'cola-cloud-all pack missing.' });
      return;
    }
    const csvPath = path.join(BATCH_DIR, set.csvFile);
    if (!existsSync(csvPath)) {
      response.status(404).json({ kind: 'validation', message: 'Pack CSV missing.' });
      return;
    }
    const csv = readFileSync(csvPath, 'utf8');
    const csvRows = parseCsvRows(csv);
    if (csvRows.length === 0) {
      response.status(404).json({ kind: 'validation', message: 'Pack CSV empty.' });
      return;
    }

    const requestedId = typeof request.query.id === 'string' ? request.query.id : null;
    const requestedIndex =
      typeof request.query.index === 'string'
        ? Number.parseInt(request.query.index, 10)
        : null;

    // Join CSV rows (keyed by filename) to manifest image cases (which
    // carry the beverage type + source subdir). The CSV is the source
    // of truth for application field values; the manifest is the source
    // of truth for the on-disk asset path.
    const manifestByFilename = new Map(
      set.imageCases.map((entry) => [path.basename(entry.assetPath), entry])
    );
    const joined = csvRows
      .map((row) => {
        const meta = manifestByFilename.get(row.filename);
        if (!meta) return null;
        return { row, meta };
      })
      .filter((entry): entry is { row: CsvRow; meta: ManifestSet['imageCases'][number] } => entry !== null);

    if (joined.length === 0) {
      response.status(404).json({ kind: 'validation', message: 'No matching manifest rows.' });
      return;
    }

    let picked: { row: CsvRow; meta: ManifestSet['imageCases'][number] };
    if (requestedId) {
      picked =
        joined.find(({ meta }) => meta.id === requestedId) ?? joined[0]!;
    } else if (requestedIndex != null && Number.isFinite(requestedIndex)) {
      const n = ((requestedIndex % joined.length) + joined.length) % joined.length;
      picked = joined[n]!;
    } else {
      picked = joined[Math.floor(Math.random() * joined.length)]!;
    }

    // Build the fields payload with the same keys the client's
    // IntakeForm uses.
    const { row, meta } = picked;
    const fields = {
      brandName: row.brand_name ?? '',
      fancifulName: row.fanciful_name ?? '',
      classType: row.class_type ?? '',
      alcoholContent: row.alcohol_content ?? '',
      netContents: row.net_contents ?? '',
      applicantAddress: row.applicant_address ?? '',
      origin: row.origin ?? '',
      country: row.country ?? '',
      formulaId: row.formula_id ?? '',
      appellation: row.appellation ?? '',
      vintage: row.vintage ?? ''
    };

    const image = {
      id: meta.id,
      source: meta.source,
      filename: path.basename(meta.assetPath),
      url: `/api/eval/label-image/${encodeURIComponent(meta.source)}/${encodeURIComponent(path.basename(meta.assetPath))}`,
      beverageType: meta.beverageType
    };

    response.setHeader('cache-control', 'no-store');
    response.json({ image, fields });
  });

  // Serves an image from the evals/labels/assets subtree.
  // `source` is a subdirectory like "cola-cloud" or "supplemental-generated".
  // `filename` is the base image filename. Path traversal is blocked.
  app.get('/api/eval/label-image/:source/:filename', (request, response) => {
    const { source, filename } = request.params;
    if (!source || !filename) {
      response.status(400).end();
      return;
    }

    if (
      source.includes('..') ||
      source.includes('/') ||
      source.includes('\\') ||
      filename.includes('..') ||
      filename.includes('/') ||
      filename.includes('\\')
    ) {
      response.status(400).end();
      return;
    }

    const resolved = path.join(LABELS_ROOT, source, filename);
    if (!resolved.startsWith(LABELS_ROOT)) {
      response.status(400).end();
      return;
    }

    if (!existsSync(resolved)) {
      response.status(404).end();
      return;
    }

    const stats = statSync(resolved);
    if (!stats.isFile()) {
      response.status(404).end();
      return;
    }

    response.setHeader('content-type', mimeForFilename(filename));
    response.setHeader('cache-control', 'public, max-age=3600');
    response.setHeader('content-length', String(stats.size));
    createReadStream(resolved).pipe(response);
  });
}
